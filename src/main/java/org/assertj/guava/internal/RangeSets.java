    failIfNull(range);
    // Should pass if both actual and expected are empty
    if (actual.isEmpty() && !range.iterator().hasNext()) return;
    failIfEmpty(range);
    assertRangeSetIntersectsAnyOfGivenValues(info, actual, toArray(range, Range.class));
